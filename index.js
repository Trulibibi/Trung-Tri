let count = 0; // Khởi tạo biến đếm

function increment() {
  count++; // Tăng biến đếm lên 1
  document.getElementById("count-el").textContent = count; // Cập nhật số hiển thị trên trang
}
function decrement() {
  count--; // Tăng biến đếm lên 1
  document.getElementById("count-el").textContent = count; // Cập nhật số hiển thị trên trang
}
